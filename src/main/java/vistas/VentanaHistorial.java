   public void mostrarHistorial(String id) {
    ArrayList<AtencionClinicaDTO> historial = historialControlador.filtrarAtencionesPorIdMascota(id);
    DefaultTableModel modelo = (DefaultTableModel) tablaHistorial.getModel();
    modelo.setRowCount(0);

    String nombreMascota = "";
    String propietarioMascota = "";
    MascotaDTO mascota = mascotaControlador.buscarMascota(id);
    if (mascota != null) {
        nombreMascota = mascota.getNombre();
        propietarioMascota = mascota.getDocumentoProp();
    }

    for (AtencionClinicaDTO atencion : historial) {
        String tipo = "";
        String diagnostico = "";
        String tratamiento = "";
        String nomVacuna = "";
        String dosis = "";

        if (atencion instanceof ConsultaDTO consulta) {
            tipo = "Consulta";
            diagnostico = consulta.getDiagnostico();
            tratamiento = consulta.getTratamiento();
        } else if (atencion instanceof VacunaDTO vacuna) {
            tipo = "Vacuna";
            nomVacuna = vacuna.getTipoVacuna();
            dosis = vacuna.getDosis();
        }

        modelo.addRow(new Object[] {
            atencion.getIdMascota(),
            nombreMascota,
            tipo,
            atencion.getFecha(),
            diagnostico,
            tratamiento,
            nomVacuna,
            dosis,
            atencion.getNombreVeterinario(),
            propietarioMascota
        });
    }
}

