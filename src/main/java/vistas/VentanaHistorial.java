    private void bttMostrarDetallesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttMostrarDetallesActionPerformed
        // TODO add your handling code here:
    int filaSeleccionada = tablaHistorial.getSelectedRow();
    // Verifica si hay una fila seleccionada
    if (filaSeleccionada == -1) {
        JOptionPane.showMessageDialog(null, "Selecciona una fila primero.");
        return;
    }
    // Lee el tipo desde la tabla
    String tipo = tablaHistorial.getValueAt(filaSeleccionada, 2).toString();
    // Muestra datos según el tipo
    if (tipo.equals("Consulta")) {
        String diagnostico = tablaHistorial.getValueAt(filaSeleccionada, 4).toString();
        String tratamiento = tablaHistorial.getValueAt(filaSeleccionada, 5).toString();
        JOptionPane.showMessageDialog(null, "Consulta: \nDiagnóstico: " + diagnostico + "\nTratamiento: " + tratamiento);
    } else if (tipo.equals("Vacuna")) {
        String tipoVacuna = tablaHistorial.getValueAt(filaSeleccionada, 6).toString();
        String dosis = tablaHistorial.getValueAt(filaSeleccionada, 7).toString();
        JOptionPane.showMessageDialog(null, "Vacuna:\nTipo de Vacuna: " + tipoVacuna + "\nDosis: " + dosis);
    } else {
        JOptionPane.showMessageDialog(null, "Tipo de atención no reconocido.");
    }
    }//GEN-LAST:event_bttMostrarDetallesActionPerformed

