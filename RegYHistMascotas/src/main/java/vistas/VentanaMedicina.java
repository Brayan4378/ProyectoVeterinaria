    public void agregarDatosTabla() {
        // Obtener el modelo de la tabla
        DefaultTableModel modelo = (DefaultTableModel) tablaConsultas.getModel();
        // Lista de consultas 
        ArrayList<Consulta> listaConsultas = consultaControlador.obtenerTodas();
        // Lista de mascotas
        ArrayList<MascotaDTO> listaMascotas = mascotaControlador.obtenerTodasMasc();

        for (Consulta cons : listaConsultas) {
            String nombreMascota = "";

        for (MascotaDTO m : listaMascotas) {
            if (m.getId().equals(cons.getIdMascota())) {
                nombreMascota = m.getNombre();
                break;
            }
        }

        Object[] fila = {
            cons.getIdMascota(),
            nombreMascota,
            cons.getDiagnostico(),
            cons.getTratamiento(),
            cons.getDocumentoProCons()
        };
        modelo.addRow(fila);
    }
}

        

