    private void bttRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttRegistrarActionPerformed
        // TODO add your handling code here:
        String idMascota = txtIDMasc.getText().trim();
        String docProp = txtDocProp.getText().trim();
        String diagnostico = txtDiagn.getText().trim();
        String tratamiento = txtTrata.getText().trim();
        LocalDate fecha = LocalDate.now();

        // Validar campos vac√≠os
        if (idMascota.isBlank() || docProp.isBlank() || diagnostico.isBlank() || tratamiento.isBlank()) {
            JOptionPane.showMessageDialog(this, "Completa todos los campos.");
            return;
        }

        // Verificar si la mascota existe
        MascotaDTO mascota = mascotaControlador.buscarPorIdYDocumento(idMascota, docProp);
        if (mascota == null) {
            JOptionPane.showMessageDialog(this, "Mascota no registrada con ese ID y documento.");
            return;
        }

        // Generar c√≥digo de consulta
        String codigo = IDGenerator.generarCodigoConsulta();

        // Registrar la consulta
        boolean registrada = consultaControlador.registrarConsulta(codigo, fecha, diagnostico, tratamiento, docProp, mascota.getNombre());

        if (registrada) {
            JOptionPane.showMessageDialog(this, "Consulta m√©dica registrada con c√≥digo: " + codigo);
            limpiarCampos(); 
            agregarDatosTabla(); 
        } else {
            JOptionPane.showMessageDialog(this, "Error al registrar la consulta.", "Error", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_bttRegistrarActionPerformed

